/*
 *  Simulate oscillator 3 for read-back
 */

void MOS6581::update_osc3()
{
	uint32_t now = TheC64->CycleCounter();

	uint8_t v3_ctrl = regs[0x12];	// Voice 3 control register
	if (v3_ctrl & 0x08) {			// Test bit
		fake_v3_count = 0;
	} else {
		uint32_t elapsed = now - fake_v3_update_cycle;
		uint32_t add = (regs[0x0f] << 8) | regs[0x0e];
		fake_v3_count = (fake_v3_count + add * elapsed) & 0xffffff;
	}

	fake_v3_update_cycle = now;
}


/*
 *  Oscillator 3 read-back
 */

uint8_t MOS6581::read_osc3()
{
	update_osc3();

	uint8_t v3_ctrl = regs[0x12];   // Voice 3 control register
	if (v3_ctrl & 0x10) {			// Triangle wave
		// TODO: ring modulation from voice 2
		if (fake_v3_count & 0x800000) {
			return (fake_v3_count >> 15) ^ 0xff;
		} else {
			return fake_v3_count >> 15;
		}
	} else if (v3_ctrl & 0x20) {	// Sawtooth wave
		return fake_v3_count >> 16;
	} else if (v3_ctrl & 0x40) {	// Rectangle wave
		uint32_t pw = ((regs[0x11] & 0x0f) << 8) | regs[0x10];
		if (fake_v3_count > (pw << 12)) {
			return 0xff;
		} else {
			return 0x00;
		}
	} else if (v3_ctrl & 0x80) {	// Noise wave
		return sid_random();
	} else {
		// TODO: combined waveforms
		return 0;
	}
}


/*
 *  EG 3 read-back
 */

uint8_t MOS6581::read_env3() const
{
	return (uint8_t)(fake_v3_eg_level >> 16);
}


/*
 *  Get SID state
 */

void MOS6581::GetState(MOS6581State *ss) const
{
	ss->freq_lo_1 = regs[0];
	ss->freq_hi_1 = regs[1];
	ss->pw_lo_1 = regs[2];
	ss->pw_hi_1 = regs[3];
	ss->ctrl_1 = regs[4];
	ss->AD_1 = regs[5];
	ss->SR_1 = regs[6];

	ss->freq_lo_2 = regs[7];
	ss->freq_hi_2 = regs[8];
	ss->pw_lo_2 = regs[9];
	ss->pw_hi_2 = regs[10];
	ss->ctrl_2 = regs[11];
	ss->AD_2 = regs[12];
	ss->SR_2 = regs[13];

	ss->freq_lo_3 = regs[14];
	ss->freq_hi_3 = regs[15];
	ss->pw_lo_3 = regs[16];
	ss->pw_hi_3 = regs[17];
	ss->ctrl_3 = regs[18];
	ss->AD_3 = regs[19];
	ss->SR_3 = regs[20];

	ss->fc_lo = regs[21];
	ss->fc_hi = regs[22];
	ss->res_filt = regs[23];
	ss->mode_vol = regs[24];

	ss->pot_x = 0xff;
	ss->pot_y = 0xff;

	ss->v3_update_cycle = fake_v3_update_cycle;
	ss->v3_count = fake_v3_count;
	ss->v3_eg_level = fake_v3_eg_level;
	ss->v3_eg_state = fake_v3_eg_state;
}


/*
 *  Restore SID state
 */

void MOS6581::SetState(const MOS6581State *ss)
{
	regs[0] = ss->freq_lo_1;
	regs[1] = ss->freq_hi_1;
	regs[2] = ss->pw_lo_1;
	regs[3] = ss->pw_hi_1;
	regs[4] = ss->ctrl_1;
	regs[5] = ss->AD_1;
	regs[6] = ss->SR_1;

	regs[7] = ss->freq_lo_2;
	regs[8] = ss->freq_hi_2;
	regs[9] = ss->pw_lo_2;
	regs[10] = ss->pw_hi_2;
	regs[11] = ss->ctrl_2;
	regs[12] = ss->AD_2;
	regs[13] = ss->SR_2;

	regs[14] = ss->freq_lo_3;
	regs[15] = ss->freq_hi_3;
	regs[16] = ss->pw_lo_3;
	regs[17] = ss->pw_hi_3;
	regs[18] = ss->ctrl_3;
	regs[19] = ss->AD_3;
	regs[20] = ss->SR_3;

	regs[21] = ss->fc_lo;
	regs[22] = ss->fc_hi;
	regs[23] = ss->res_filt;
	regs[24] = ss->mode_vol;

	fake_v3_update_cycle = ss->v3_update_cycle;
	fake_v3_count = ss->v3_count;
	fake_v3_eg_level = ss->v3_eg_level;
	fake_v3_eg_state = ss->v3_eg_state;

	// Stuff the new register values into the renderer
	if (the_renderer != nullptr) {
		for (unsigned i = 0; i < 25; ++i) {
			the_renderer->WriteRegister(i, regs[i]);
		}
	}
}


